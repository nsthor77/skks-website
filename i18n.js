// ===== i18n Functions =====
const i18n = {
  // Get current language (default: en)
  current() {
    return localStorage.getItem('skks_lang') || 'en';
  },
  
  // Set language
  set(lang) {
    if (!translations[lang]) return;
    localStorage.setItem('skks_lang', lang);
    document.documentElement.lang = lang;
    this.apply();
  },
  
  // Get translation
  t(key) {
    const lang = this.current();
    return translations[lang][key] || translations.en[key] || key;
  },
  
  // Apply translations to all elements
  apply() {
    const lang = this.current();
    
    // Translate text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = this.t(key);
      el.innerHTML = text;
    });
    
    // Translate placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      el.placeholder = this.t(key);
    });
    
    // Translate aria-labels
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria');
      el.setAttribute('aria-label', this.t(key));
    });
    
    // Translate option values
    document.querySelectorAll('[data-i18n-option]').forEach(el => {
      const key = el.getAttribute('data-i18n-option');
      el.textContent = this.t(key);
    });
    
    // Update language switcher buttons
    document.querySelectorAll('.lang-switch a').forEach(a => {
      a.classList.remove('active');
      if (a.getAttribute('data-lang') === lang) {
        a.classList.add('active');
      }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Update page title
    if (lang === 'en') {
      document.title = 'SKKS — Suanson Khon Kaen School';
    } else {
      document.title = 'SKKS — โรงเรียนสวนสนขอนแก่น';
    }
  },
  
  // Toggle language
  toggle() {
    this.set(this.current() === 'en' ? 'th' : 'en');
  },
  
  // Initialize
  init() {
    // Set HTML lang
    document.documentElement.lang = this.current();
    
    // Apply translations on load
    if (document.readyState === 'loading') {
      document.addEventListener('DOMContentLoaded', () => this.apply());
    } else {
      this.apply();
    }
  }
};

// Export to window
window.i18n = i18n;
window.translations = translations;

// Auto-init
i18n.init();
